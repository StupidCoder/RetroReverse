package psp

// ge_wire.go carries the GE register file through a savestate.
//
// geState (ge_raster.go) is the GE's decoded register file, and it is deliberately
// unexported — nothing outside the rasteriser has any business reading it. But gob
// only encodes exported fields, and the register file MUST be in the savestate: it
// persists across display lists by design ("a frame's FBP/matrix setup list conditions
// the draw lists that follow"), so a machine restored without it does not resume the
// machine that was saved. It rebuilds the register file from the executor's defaults
// instead, and every value the game programmed once at engine init and never re-sends
// is quietly replaced by a guess.
//
// So geWire is an exported mirror of geState, field for field, generated by reflecting
// over the type rather than typed out by hand. TestGEWireCoversGEState asserts the two
// still line up: a field added to geState and forgotten here would be dropped from the
// savestate silently, which is the exact bug this file exists to fix.

